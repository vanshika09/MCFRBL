package newdashboard.model;

public class MachineBreakdown {
	
	private String machine_sr_no;
	
	private String machine_description;
	
	private String breakdown_date;

	public String getMachine_sr_no() {
		return machine_sr_no;
	}

	public void setMachine_sr_no(String machine_sr_no) {
		this.machine_sr_no = machine_sr_no;
	}

	public String getMachine_description() {
		return machine_description;
	}

	public void setMachine_description(String machine_description) {
		this.machine_description = machine_description;
	}

	public String getBreakdown_date() {
		return breakdown_date;
	}

	public void setBreakdown_date(String breakdown_date) {
		this.breakdown_date = breakdown_date;
	}
	
	
}
